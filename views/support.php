	<h2>International</h2>
	<p><b>Funding and support programmes for researchers and students</b></p>
  
		<ul>
		<li><a href="https://euraxess.ec.europa.eu/ukraine" target="_blank">ERA4Ukraine</a><br>
		European Commission / Euraxess initiative to support researchers of Ukraine by providing an overview of all existing actions at European and national levels.</li>
	<br>
		<li><a href="https://www.scholarsatrisk.org/" target="_blank">Scholars at Risk</a><br>
		Scholars at Risk protects scholars suffering grave threats to their lives, liberty and well-being by arranging temporary research and teaching positions at institutions in our network as well as by providing advisory and referral services.</li>
	<br>
		<li><a href="https://euraxess.ec.europa.eu/jobs/science4refugees" target="_blank">Science4refugees | EURAXESS</a><br>
		The Science4Refugees initiative and EURAXESS portal provide research refugee-friendly internships, part-time and full-time jobs, access to an European Research Community, as well as a complete range of information and support services on working and living in Europe. Ukrainian researchers are eligible to benefit from the Science4Refugees initiative without the need of holding the refugee status.</li>
	<br>
		<li><a href="https://eua.eu/resources/projects/759-inspireurope.html" target="_blank">InSPIREurope</a><br>
		InSPIREurope is a Europe-wide initiative to support researchers who are at risk due to discrimination, persecution, suffering or violence. Funded under the European Commission’s Marie Skłodowska-Curie Actions, the initiative is hosted at Maynooth University in Ireland and coordinated by <a href="https://www.scholarsatrisk.org/scholars-at-risk-europe/" target="_blank">Scholars at Risk Europe</a>.</li>
	<br>
		<li><a href="https://www.scholarrescuefund.org/for-scholars/" target="_blank">Institute of International Education's Scholar Rescue Fund (IIE-SRF)</a><br>
		IIE-SRF is the only global program that arranges, funds, and supports fellowships for threatened and displaced scholars at partnering higher education institutions worldwide. These year-long fellowships of $25,000USD support temporary academic positions at institutions of higher learning anywhere in the world where IIE-SRF fellows can continue their work in safety. In most cases, IIE-SRF fellows are eligible to apply for a second and final year of fellowship support.</li>
	<br>
		<li><a href="https://opensocietyuniversitynetwork.org/education/threatened-scholars-initiative/" target="_blank">Open Society University Network</a><br>
		TSI offers Fellowships in OSUN institutions for scholars who have lost their academic positions or cannot remain in their home countries due to threats or actions from authoritarian regimes, persecution for their views or identities, or other risks.  Fellowship activities can include teaching, research, writing and other creative activities, and participating in the scholarly and cultural life of host institutions and the broader OSUN community.</li>
	<br>	
		<li><a href="https://www.bard.edu/tsi/" target="_blank">Threatened Scholars Integration Initiative (TSI)</a><br>
		The Threatened Scholars Integration Initiative (TSI) is a project of the <a href="https://opensocietyuniversitynetwork.org/" target="_blank">Open Society University Network</a> (OSUN). OSUN is a network of over forty <a href="https://opensocietyuniversitynetwork.org/about/members/" target="_blank">teaching and research institutions</a> around the world. TSI offers Fellowships in OSUN institutions for scholars who have lost their academic positions or cannot remain in their home countries due to threats or actions from authoritarian regimes, persecution for their views or identities, or other risks.  Fellowship activities can include teaching, research, writing and other creative activities, and participating in the scholarly and cultural life of host institutions and the broader OSUN community.</li>
	<br>
		<li>Emergency Fund for Ukrainian researchers by the European Research Network on Signal Transduction (ERNEST), COST Action<br>
		<a href="https://sites.google.com/view/ernestcostaction-ukraine-help" target="_blank">https://sites.google.com/view/ernestcostaction-ukraine-help</a><br>
		<i>Short-term scientific missions</i>. Research stays, usually 2-3 months in any EU research lab working on cell signaling. Max financial support 4,000 Euro. <i>Virtual mobility</i>. Support of research and knowledge dissemination activities that can be performed without physical relocation. Max financial support 1,500 Euro. Note: for both funding types only 50% prepayment is allowed. The rest of money becomes available after completion of stay/virtual activity. 	General eligibility: Researchers affiliated to any legal entity in Ukraine (for example, schools and universities, research centers, governmental institutions, or private companies).</li>
	<br>
		<li>IIE Emergency Student Fund <a href="https://www.iie.org/Programs/Emergency-Student-Fund" target="_blank">https://www.iie.org/Programs/Emergency-Student-Fund</a><br>
		IIE's Emergency Student Fund (ESF) provides grants to international college students in the U.S. when natural disasters, war, or other crises threaten their education. Many face financial pressure to return home or drop out because their family circumstances have changed dramatically. IIE helps these students cover essential needs, including basic living expenses.</li>
	<br>
		<li>IIE Odyssey Scholarship <a href="https://www.iie.org/en/Programs/IIE-Odyssey-Scholarship" target="_blank">https://www.iie.org/en/Programs/IIE-Odyssey-Scholarship</a><br>
		A comprehensive scholarship package for student refugees and displaced individuals. The funding covers tuition, housing and living expenses for students pursuing four-year bachelor’s degrees or two-year master’s degrees, as well as advising services, community support and practical resources they may need along their journey.</li>

<br>
		
		
	</ul>	
